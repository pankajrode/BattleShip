package com.thoughtworks.battleship;

public final class Size {
	public final int WIDTH ;
	public final int HEIGHT;
	
	public Size(int width, int height) {
		this.WIDTH = width;
		this.HEIGHT = height;
	}

	public int getWIDTH() {
		return WIDTH;
	}

	public int getHEIGHT() {
		return HEIGHT;
	}

}
